#include <stdio.h>
#include <stdlib.h>

int main()
{
    long long n,s,sk=0;
    long long v,t,i,nv,nt,vu=0;
    scanf("%lld%lld",&n,&s);
    scanf("%lld%lld",&t,&v);
    printf("0\n");
    for(i=1;i<n;i++)
    {
        scanf("%lld%lld",&nt,&nv);
        sk=s-(v*(nt-t));
        if(sk<0||v>nv) {v=nv;t=nt;printf("0\n");continue;}
        vu=s*v/sk;
        if(vu<nv) {printf("%lld\n",nv-vu);v=vu;}
        else {printf("0\n");v=nv;}
        t=nt;
    }
    return 0;
}
